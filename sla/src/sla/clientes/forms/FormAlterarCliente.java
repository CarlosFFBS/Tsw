        char c = evt.getKeyChar();
        if (!(Character.isDigit(c))) {
            evt.consume();
        }
        int numerocaracter = 8;

        if (jFormattedTextFieldNovocepCliente.getText().length() >= numerocaracter) {
            evt.consume();
        }
