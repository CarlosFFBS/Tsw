    @Override
    public void actionPerformed(ActionEvent ae) {
        if (ae.getSource() == jRadioButtonPF) {
            jRadioButtonSexoMasculino.setVisible(true);
            jRadioButtonSexoFeminino.setVisible(true);
            jLabelSexoCliente.setVisible(true);
            if (jLabelOperacaoCpfCNPJ.getText().equals("CNPJ")) {
                jLabelOperacaoCpfCNPJ.setText("CPF");
                jLabelOperacaoNomeRazaoSocial.setText("NOME");
                mascaraCNPJ.uninstall();
                mascaraCPF.install(jFormattedTextFieldOperacaoCpfCnpj);

            }

        }

        if (ae.getSource() == jRadioButtonPJ) {
            jRadioButtonSexoMasculino.setVisible(false);
            jRadioButtonSexoFeminino.setVisible(false);
            jLabelSexoCliente.setVisible(false);

            if (jLabelOperacaoCpfCNPJ.getText().equals("CPF")) {
                jLabelOperacaoCpfCNPJ.setText("CNPJ");
                jLabelOperacaoNomeRazaoSocial.setText("RAZÃO SOCIAL");
                mascaraCPF.uninstall();
                mascaraCNPJ.install(jFormattedTextFieldOperacaoCpfCnpj);

            }
        }

    }
