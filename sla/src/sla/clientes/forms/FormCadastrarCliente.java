    private void jButtonOperacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOperacaoActionPerformed
        NegocioCliente cliente = new NegocioCliente();

        try {
            if (jLabelOperacaoCpfCNPJ.getText().equals("CPF")) {
                ClientePessoaFisica PF = new ClientePessoaFisica();
                PF.setBairro(jTextFieldbairro.getText());
                PF.setCep(jFormattedTextFieldcepCliente.getText());
                PF.setCidade(jTextFieldcidade.getText());
                PF.setCli_Cpf(jFormattedTextFieldOperacaoCpfCnpj.getText());
                PF.setComplemento(jTextFieldcomplemento.getText());
                PF.setEmail(jTextFieldemail.getText());
                PF.setEstado(jTextFielduf.getText());
                PF.setLogradouro(jTextFieldEnderecoCliente.getText());
                PF.setNome(jTextFieldOperacaoNomeRazaoSocial.getText());
                PF.setNumeroLogradouro(jTextFieldnumeroEndereco.getText());
                PF.setTelefone(jFormattedTextFieldtelefoneCliente.getText());
                if (jRadioButtonSexoMasculino.isSelected()) {
                    PF.setSexo("M");
                }else  if (jRadioButtonSexoFeminino.isSelected()) {
                    PF.setSexo("F");
                }
                cliente.cadastrarClientePF(PF);
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso.");
                jFormattedTextFieldOperacaoCpfCnpj.setText("");
                jTextFieldOperacaoNomeRazaoSocial.setText("");
                jFormattedTextFieldtelefoneCliente.setText("");
                jFormattedTextFieldcepCliente.setText("");
                jTextFieldEnderecoCliente.setText("");
                jTextFieldnumeroEndereco.setText("");
                jTextFieldcomplemento.setText("");
                jTextFieldbairro.setText("");
                jTextFieldcidade.setText("");
                jTextFielduf.setText("");
                jTextFieldemail.setText("");
                jRadioButtonSexoMasculino.setSelected(false);
                jRadioButtonSexoFeminino.setSelected(false);

            }

            if (jLabelOperacaoCpfCNPJ.getText().equals("CNPJ")) {
                ClientePessoaJuridica PJ = new ClientePessoaJuridica();
                PJ.setBairro(jTextFieldbairro.getText());
                PJ.setCep(jFormattedTextFieldcepCliente.getText());
                PJ.setCidade(jTextFieldcidade.getText());
                PJ.setCli_Cnpj(jFormattedTextFieldOperacaoCpfCnpj.getText());
                PJ.setComplemento(jTextFieldcomplemento.getText());
                PJ.setEmail(jTextFieldemail.getText());
                PJ.setEstado(jTextFielduf.getText());
                PJ.setLogradouro(jTextFieldEnderecoCliente.getText());
                PJ.setRazaoSocial(jTextFieldOperacaoNomeRazaoSocial.getText());
                PJ.setNumeroLogradouro(jTextFieldnumeroEndereco.getText());
                PJ.setTelefone(jFormattedTextFieldtelefoneCliente.getText());
                cliente.cadastrarClientePJ(PJ);
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso.");
                jFormattedTextFieldOperacaoCpfCnpj.setText("");
                jTextFieldOperacaoNomeRazaoSocial.setText("");
                jFormattedTextFieldtelefoneCliente.setText("");
                jFormattedTextFieldcepCliente.setText("");
                jTextFieldEnderecoCliente.setText("");
                jTextFieldnumeroEndereco.setText("");
                jTextFieldcomplemento.setText("");
                jTextFieldbairro.setText("");
                jTextFieldcidade.setText("");
                jTextFielduf.setText("");
                jTextFieldemail.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButtonOperacaoActionPerformed

    private void jTextFieldufKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldufKeyTyped
      // Teste de uso de eventos para limitar o campo estado somente com caracteres e com limite de 2 caracteres para o estado.
        char c = evt.getKeyChar();
        if (!(Character.isAlphabetic(c)) || c == ' ') {
            evt.consume();
        }
        int numerocaracter = 2;

        if (jTextFielduf.getText().length() >= numerocaracter) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldufKeyTyped

