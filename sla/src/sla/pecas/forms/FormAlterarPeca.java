    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            Peca peca = new Peca();
            NegocioPeca a = new NegocioPeca();
            Operacao op = new Operacao();
            peca.setCodigo(Integer.parseInt(jTextFieldCodigoPeca.getText().trim()));
            peca.setDescricao(jTextFieldNomePeca.getText());
            peca.setPreco(Float.parseFloat(jTextFieldValorPeca.getText().trim()));
            String textoNovoNomePeca = jTextFieldNovoNomePeca.getText();
            String textoNovoPrecoPeca = jTextFieldNovoPrecoPeca.getText();
           

            if ( (textoNovoPrecoPeca.trim().isEmpty()== false)) {
                float preco = op.formatadorVirgulaParaPonto(jTextFieldNovoPrecoPeca.getText());
                peca.setPreco(preco);

            }
            if ((textoNovoNomePeca.trim().isEmpty() == false)) {
                peca.setDescricao(textoNovoNomePeca);

            }
           
            a.alterarPeca(peca);
            JOptionPane.showMessageDialog(null, "Peça atualizada com sucesso");
            jTextFieldNomePeca.setText(peca.getDescricao());
            jTextFieldValorPeca.setText(String.valueOf(peca.getPreco()));
            jTextFieldNovoNomePeca.setText("");
            jTextFieldNovoPrecoPeca.setText("");
        } //Tratamento para quando o programa não conseguir converter o  campo de texto referente ao preço para float ou o código para integer
        catch (NumberFormatException ex) {
            try {
                //Teste de conversão para o código da peça
                int teste = Integer.parseInt(jTextFieldCodigoPeca.getText().trim());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Não foi possível converter o código da peça para o valor informado.");
            }
            //Teste de conversão para o preço da peça
            try {
                float preco = Float.parseFloat(jTextFieldNovoPrecoPeca.getText().trim());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Não foi possivel converter o preço para o valor informado. Preencha o campo usando um número decimal.");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            jTextFieldNovoPrecoPeca.setText("");
            jTextFieldNovoNomePeca.setText("");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

