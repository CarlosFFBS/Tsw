public void RecebeCombobox(String ItemSelecionado) throws Exception {
        Peca peca = new Peca();
        DadosPeca a = new DadosPeca();
        peca.setDescricao(ItemSelecionado);

        ArrayList<Peca> lista = a.consultarPeca(peca);

        jTextFieldCodigoPeca.setText(String.valueOf(lista.get(0).getCodigo()));
        jTextFieldNomePeca.setText(lista.get(0).getDescricao());
        jTextFieldValorPeca.setText(String.valueOf(lista.get(0).getPreco()));
        jTextFieldNovoNomePeca.requestFocus();
    }

    public void RecebeJtable(Integer pecaSelecionada) throws Exception {

        Peca peca = new Peca();
        NegocioPeca a = new NegocioPeca();
        peca.setCodigo(pecaSelecionada);
        ArrayList<Peca> pecaConsultada = a.consultarPeca(peca);
        peca = pecaConsultada.get(0);
        jTextFieldCodigoPeca.setText(String.valueOf(peca.getCodigo()));
        jTextFieldNomePeca.setText(String.valueOf(peca.getDescricao()));
        jTextFieldValorPeca.setText(String.valueOf(peca.getPreco()));
    }

