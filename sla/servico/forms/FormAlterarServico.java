    private void jButtonAtualizarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarServicoActionPerformed
        try {

            Servico servico = new Servico();
            NegocioServico a = new NegocioServico();
            Operacao op = new Operacao();
            servico.setCodServico(Integer.parseInt(jTextFieldCodServico.getText()));
            servico.setDescricao(jTextFieldNomeServico.getText());
            servico.setPreco(Float.parseFloat(jTextFieldValorServico.getText()));
            String textoNovoNomeServico = jTextFieldNovoNomeServico.getText();
            String textoNovoPrecoServico = jTextFieldNovoValorServico.getText();

            if (textoNovoPrecoServico.trim().isEmpty() == false) {
                float preco = op.formatadorVirgulaParaPonto(textoNovoPrecoServico.trim());
                servico.setPreco(preco);

            }
            if (textoNovoNomeServico.trim().isEmpty() == false) {
                servico.setDescricao(textoNovoNomeServico);

            }

            a.alterarServico(servico);
            JOptionPane.showMessageDialog(null, "Serviço atualizado com sucesso");
            jTextFieldNomeServico.setText(servico.getDescricao());
            jTextFieldValorServico.setText(String.valueOf(servico.getPreco()));
            jTextFieldNovoNomeServico.setText("");
            jTextFieldNovoValorServico.setText("");
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            jTextFieldNovoValorServico.setText("");
            jTextFieldNovoNomeServico.setText("");

        }

    }//GEN-LAST:event_jButtonAtualizarServicoActionPerformed

