public void RecebejTable(Integer ServicoSelecionado) throws Exception {
        Servico servico = new Servico();
        NegocioServico a = new NegocioServico();
        servico.setCodServico(ServicoSelecionado);
        ArrayList<Servico> servicoConsultado = a.consultarServico(servico);
        servico = servicoConsultado.get(0);
        jTextFieldCodServico.setText(String.valueOf(servico.getCodServico()));
        jTextFieldNomeServico.setText(String.valueOf(servico.getDescricao()));
        jTextFieldValorServico.setText(String.valueOf(servico.getPreco()));

    }

